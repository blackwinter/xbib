package org.xbib.oai.client.identify;

import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;
import org.w3c.dom.Document;
import org.w3c.dom.Element;
import org.w3c.dom.NodeList;
import org.xbib.io.Request;
import org.xbib.io.http.HttpResponse;
import org.xbib.oai.DefaultOAIResponseListener;
import org.xml.sax.InputSource;
import org.xml.sax.SAXException;

import javax.xml.parsers.DocumentBuilder;
import javax.xml.parsers.DocumentBuilderFactory;
import javax.xml.parsers.ParserConfigurationException;
import java.io.IOException;
import java.io.StringReader;

public class IdentifyResponseListener extends DefaultOAIResponseListener {

    private final static Logger logger = LogManager.getLogger(IdentifyResponseListener.class.getName());

    private final IdentifyRequest request;

    private final IdentifyResponse response;

    private StringBuilder body;

    public IdentifyResponseListener(IdentifyRequest request) {
        this.request = request;
        this.response = new IdentifyResponse(request);
        this.body = new StringBuilder();
    }

    public IdentifyResponse getResponse() {
        return response;
    }

    @Override
    public void onReceive(Request request, CharSequence message) throws IOException {
        body.append(message);
    }

    @Override
    public void receivedResponse(HttpResponse result) throws IOException {
        try {
            DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();
            DocumentBuilder db = dbf.newDocumentBuilder();
            InputSource is = new InputSource(new StringReader(body.toString()));
            Document doc = db.parse(is);
            response.setGranularity(getString("granularity", doc.getDocumentElement()));
        } catch (ParserConfigurationException e) {
            //
        } catch (SAXException e) {
            //
        }

    }

    protected String getString(String tagName, Element element) {
        NodeList list = element.getElementsByTagName(tagName);
        if (list != null && list.getLength() > 0) {
            NodeList subList = list.item(0).getChildNodes();
            if (subList != null && subList.getLength() > 0) {
                return subList.item(0).getNodeValue();
            }
        }
        return null;
    }
}